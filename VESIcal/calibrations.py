def return_calibration_type(model_name):
    """
    Returns metadata for the calibration dataset for specified model

    Parameters
    ----------
    model: string
        String of model name.

    Returns
    -------
    dict
        Metadata on the dataset. A dict is returned with parameters CO2, H2O,
        or mixed set to True or False. True indicates that experiments where
        performed using that fluid type. For example, a model may be able to
        calculate mixed H2O-CO2 solubilities, but it's dataset may be made up
        only of pure-CO2 and pure-H2O data. In this case, a model's metadata
        would be {'CO2': True, 'H2O': True, 'Mixed': False}.

        This value expresses whether experiments used to parameterized models
        are pure-CO2, pure-H2O, or  mixed H2O-CO2 experiments. In many cases,
        pure-CO2 experiments cannot be distinguished from mixed H2O-CO2
        experiments, in which case the experiments are classified as 'Mixed'.
    """

    if model_name == 'AllisonCarbon':
        return {'CO2': False, 'H2O': False, 'Mixed': True}

    if model_name == 'Dixon':
        return {'CO2': True, 'H2O': True, 'Mixed': False}

    if model_name == 'DixonCarbon':
        return {'CO2': True, 'H2O': False, 'Mixed': False}

    if model_name == 'DixonWater':
        return {'CO2': False, 'H2O': True, 'Mixed': False}

    if model_name == 'IaconoMarziano':
        return {'CO2': False, 'H2O': True, 'Mixed': True}

    if model_name == 'IaconoMarzianoCarbon':
        return {'CO2': False, 'H2O': False, 'Mixed': True}

    if model_name == 'IaconoMarzianoWater':
        return {'CO2': False, 'H2O': True, 'Mixed': False}

    if model_name == 'Liu':
        return {'CO2': False, 'H2O': True, 'Mixed': True}

    if model_name == 'LiuCarbon':
        return {'CO2': False, 'H2O': False, 'Mixed': True}

    if model_name == 'LiuWater':
        return {'CO2': False, 'H2O': True, 'Mixed': False}

    if model_name == 'MagmaSat':
        return{'CO2': True, 'H2O': True, 'Mixed': True}

    if model_name == 'MooreWater':
        return {'CO2': False, 'H2O': False, 'Mixed': True}

    if model_name == 'Shishkina':
        return {'CO2': False, 'H2O': True, 'Mixed': True}

    if model_name == 'ShishkinaCarbon':
        return {'CO2': False, 'H2O': False, 'Mixed': True}

    if model_name == 'ShishkinaWater':
        return {'CO2': False, 'H2O': True, 'Mixed': False}


